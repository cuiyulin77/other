import scrapy


class GovPeopleItem(scrapy.Item):
    position = scrapy.Field()      # 职位
    department = scrapy.Field()    # 部门
    name = scrapy.Field()          # 人名
    people_url = scrapy.Field()    # 个人url
    province = scrapy.Field()      # 省
    city = scrapy.Field()          # 市
    pass
